class CrimsonForgeError(Exception):
	"""
	The base exception that is inherited by all custom Crimson Forge error
	classes.
	"""
	def __init__(self, message=''):
		super(CrimsonForgeError, self).__init__()
		self.message = message

class AnalysisError(CrimsonForgeError):
	pass
